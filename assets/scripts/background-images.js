function drawNeighbourlyUnderline() {
    var tl = new TimelineMax();

    tl.set('.neighbourlyLineHolder', {
            css: {
                opacity: 1
            }
        })
        .set('#underline', {
            transformOrigin: "0% 0%"
        })
        .fromTo("#underline", 3, {
            drawSVG: "0% 0%"
        }, {
            drawSVG: true
        });
}